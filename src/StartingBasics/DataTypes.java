package StartingBasics;

public class DataTypes {
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static boolean boo;
	static String str;
	public static void main(String[] args) {
		/*Total Datatypes Classified into 4 Categories.
	    DataTypes :
	    Data Types are nothing but that are types of values we use in java programming
	    
	    1> Primitive Data Types
		1.Integer
			-byte   1byte
			-short  2byte
			-int    4byte
			-long   8byte
		2.Float
			-float  4byte
			-double 8byte
		3.Character
			-char   2byte
		4.Boolean
			-true   1bit
			-false  1bit
			
		2> Non-Primitive Data Types
		Example- String,Array,Class etc.  --> No fix length
		*/
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(boo);
		System.out.println(str);
		
		
	}

}
