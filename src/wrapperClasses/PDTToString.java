package wrapperClasses;

public class PDTToString {
	
//we can convert all PDT into String with the help of valueOf()
	public static void main(String[] args) {

		//PDT to String
		int a=100;
		String obj=String.valueOf(a); //method overloading
		
		byte b=12;
		String obj1=String.valueOf(b); //method overloading
		
		short c=12365;
		String obj2=String.valueOf(c); //method overloading
		
		long d=100000000;
		String obj3=String.valueOf(d); //method overloading
		
		float e=10.25f;
		String obj4=String.valueOf(e); //method overloading
		
		double f=125.25;
		String obj5=String.valueOf(f); //method overloading
		
		char g='a';
		String obj6=String.valueOf(g); //method overloading
		 
		boolean h=true;
		String obj7=String.valueOf(h); //method overloading

		
		
	}
}
