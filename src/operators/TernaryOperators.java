package operators;

public class TernaryOperators {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		/*
		
		Ternary Operators
		1.Conditional  
			-The Operators which takes three values at a time 		
			-Syntax :      condition ? Task1 : Task2
		*/
		System.out.println("--------------------------------------------");
		System.out.println(a>b?a:b);
		
	}

}
